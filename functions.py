def square(x):
    return x*x

for i in range(10):
    print(f"Il quadrato di {i} e' {square(i)}")   

#-----------------------------------------------------

def square2(x):
    return (x*x)*2

for i in range(10):
    print(f"Il doppio del quadrato di {i} e' {square2(i)}")

